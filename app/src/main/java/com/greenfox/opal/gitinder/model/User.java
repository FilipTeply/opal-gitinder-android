package com.greenfox.opal.gitinder.model;

public class User {
    String username;
    String accessToken;
    String appToken;

    public User() {
    }

    public String getUsername() {
        return username;
    }

    public String getAccessToken() {
        return accessToken;
    }

    public String getAppToken() {
        return appToken;
    }
}
